package org.tdmx.core.system.lang;

import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertNotNull;

import java.util.HashMap;
import java.util.Map;

import org.junit.Before;
import org.junit.Test;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class JavaLangInvariantsTest {
	private final Logger log = LoggerFactory.getLogger(JavaLangInvariantsTest.class);

	@Before
	public void setUp() throws Exception {
	}

	@Test
	public void testMapValuesNeverNull() {
		Map<String, Object> map = new HashMap<String, Object>();

		assertNotNull(map.keySet());
		assertEquals(0, map.keySet().size());
		assertNotNull(map.values());
		assertEquals(0, map.values().size());
	}

}
