package org.tdmx.console.domain;

import java.io.Serializable;

/**
 * @author Peter
 *
 */
public final class SearchResultObject implements Serializable {

	//-------------------------------------------------------------------------
	//PUBLIC CONSTANTS
	//-------------------------------------------------------------------------
	
	//-------------------------------------------------------------------------
	//PROTECTED AND PRIVATE VARIABLES AND CONSTANTS
	//-------------------------------------------------------------------------
	private String clazz;
	private String name;
	private String id;
	
	//-------------------------------------------------------------------------
	//CONSTRUCTORS
	//-------------------------------------------------------------------------

	public SearchResultObject( String clazz, String name, String id ) {
		this.clazz = clazz;
		this.name = name;
		this.id = id;
	}
	
	//-------------------------------------------------------------------------
	//PUBLIC METHODS
	//-------------------------------------------------------------------------
	
    //-------------------------------------------------------------------------
	//PROTECTED METHODS
	//-------------------------------------------------------------------------

	//-------------------------------------------------------------------------
	//PRIVATE METHODS
	//-------------------------------------------------------------------------

	//-------------------------------------------------------------------------
	//PUBLIC ACCESSORS (GETTERS / SETTERS)
	//-------------------------------------------------------------------------
	
	public String getClazz() {
		return clazz;
	}

	public String getName() {
		return name;
	}

	public String getId() {
		return id;
	}

}
